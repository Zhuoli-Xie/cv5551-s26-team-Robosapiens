def rot_to_rpy(R):
    sy = numpy.sqrt(R[0,0]**2 + R[1,0]**2)
    singular = sy < 1e-6

    if not singular:
        roll  = numpy.arctan2(R[2,1], R[2,2])
        pitch = numpy.arctan2(-R[2,0], sy)
        yaw   = numpy.arctan2(R[1,0], R[0,0])
    else:
        roll  = numpy.arctan2(-R[1,2], R[1,1])
        pitch = numpy.arctan2(-R[2,0], sy)
        yaw   = 0

    return numpy.degrees([roll, pitch, yaw])

